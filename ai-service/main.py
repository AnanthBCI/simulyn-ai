# ---------------------------------------------------------------------------
# Chat copilot (/chat-step)
#
# Stateless step in the .NET-orchestrated tool-calling loop. The .NET side
# owns:
#   - executing tools against the org's data (in-process, multi-tenant safe)
#   - looping until the LLM answers (cap at 6 iterations)
#   - persisting nothing server-side (frontend keeps history in localStorage)
#
# This endpoint translates the canonical message + tool format used by the
# .NET API into each provider's native shape (OpenAI / Ollama / Anthropic),
# calls the model, and translates the response back. If no provider is
# configured we return a friendly "off" answer so the UI never blanks.
# ---------------------------------------------------------------------------


class ChatToolDefinition(BaseModel):
    name: str
    description: str
    parameters: dict


class ChatToolCall(BaseModel):
    id: str
    name: str
    arguments: dict


class ChatMessage(BaseModel):
    role: Literal["system", "user", "assistant", "tool"]
    content: Optional[str] = None
    toolCalls: Optional[list[ChatToolCall]] = Field(default=None, alias="toolCalls")
    toolCallId: Optional[str] = Field(default=None, alias="toolCallId")
    name: Optional[str] = None

    model_config = {"populate_by_name": True}


class ChatStepIn(BaseModel):
    messages: list[ChatMessage]
    available_tools: list[ChatToolDefinition] = Field(alias="availableTools")

    model_config = {"populate_by_name": True}


class ChatStepOut(BaseModel):
    type: Literal["tool_call", "answer"]
    tool_calls: Optional[list[ChatToolCall]] = None
    content: Optional[str] = None
    detected_language: Optional[str] = None
    provider: str


def _msgs_to_openai(messages: list[ChatMessage]) -> list[dict]:
    """Translate canonical messages to OpenAI/Ollama chat-completions shape."""
    out: list[dict] = []
    for m in messages:
        if m.role == "assistant" and m.toolCalls:
            out.append({
                "role": "assistant",
                "content": m.content or "",
                "tool_calls": [
                    {
                        "id": tc.id,
                        "type": "function",
                        "function": {
                            "name": tc.name,
                            "arguments": json.dumps(tc.arguments or {}),
                        },
                    }
                    for tc in m.toolCalls
                ],
            })
        elif m.role == "tool":
            out.append({
                "role": "tool",
                "tool_call_id": m.toolCallId or "",
                "content": m.content or "",
            })
        else:
            out.append({"role": m.role, "content": m.content or ""})
    return out


def _tools_to_openai(tools: list[ChatToolDefinition]) -> list[dict]:
    return [
        {
            "type": "function",
            "function": {
                "name": t.name,
                "description": t.description,
                "parameters": t.parameters,
            },
        }
        for t in tools
    ]


def _msgs_to_anthropic(messages: list[ChatMessage]) -> tuple[Optional[str], list[dict]]:
    """Translate canonical messages to Anthropic shape.

    Returns (system_text, messages). Anthropic takes `system` as its own param
    (not as a message), and tool replies live as `tool_result` content blocks
    inside a `user` message.
    """
    system_parts: list[str] = []
    out: list[dict] = []
    for m in messages:
        if m.role == "system":
            if m.content:
                system_parts.append(m.content)
            continue
        if m.role == "assistant" and m.toolCalls:
            blocks: list[dict] = []
            if m.content:
                blocks.append({"type": "text", "text": m.content})
            for tc in m.toolCalls:
                blocks.append({
                    "type": "tool_use",
                    "id": tc.id,
                    "name": tc.name,
                    "input": tc.arguments or {},
                })
            out.append({"role": "assistant", "content": blocks})
            continue
        if m.role == "tool":
            out.append({
                "role": "user",
                "content": [{
                    "type": "tool_result",
                    "tool_use_id": m.toolCallId or "",
                    "content": m.content or "",
                }],
            })
            continue
        out.append({"role": m.role, "content": m.content or ""})
    system = "\n\n".join(system_parts) if system_parts else None
    return system, out


def _tools_to_anthropic(tools: list[ChatToolDefinition]) -> list[dict]:
    return [
        {
            "name": t.name,
            "description": t.description,
            "input_schema": t.parameters,
        }
        for t in tools
    ]


def _safe_json_loads(raw: Any) -> dict:
    """Tool-call arguments come back as JSON strings from OpenAI; be permissive."""
    if isinstance(raw, dict):
        return raw
    if not raw:
        return {}
    try:
        return json.loads(raw)
    except Exception:
        return {}


_LEAKED_TOOL_CALL_RE = re.compile(
    r'\{\s*"name"\s*:\s*"[a-z_]+"\s*,\s*"(?:parameters|arguments)"\s*:.*?\}',
    re.DOTALL,
)
_THINK_BLOCK_RE = re.compile(r"<think>.*?</think>", re.DOTALL | re.IGNORECASE)
_OPEN_THINK_TRUNCATED_RE = re.compile(r"<think>.*", re.DOTALL | re.IGNORECASE)


def _strip_thinking(text: str) -> str:
    """Remove qwen3-style reasoning blocks the user shouldn't see.

    Qwen3 emits `<think>...</think>` reasoning before its visible answer. If the
    model finishes naturally we strip the closed block; if it ran out of tokens
    mid-thought we strip the unterminated tail too so the user doesn't see a
    half-finished reasoning trace.
    """
    cleaned = _THINK_BLOCK_RE.sub("", text or "")
    cleaned = _OPEN_THINK_TRUNCATED_RE.sub("", cleaned)
    return cleaned.strip()


def _scrub_leaked_tool_calls(text: str) -> str:
    """Strip plain-text tool-call JSON that small models sometimes leak.

    e.g. llama3.2:3b will occasionally emit `{"name": "list_projects", "parameters": {}}`
    in its answer text instead of using the structured tool_calls channel. Showing that
    raw protocol text to a user is confusing, so we scrub it before returning.
    """
    cleaned = _LEAKED_TOOL_CALL_RE.sub("", text or "").strip()
    return cleaned


def _is_qwen3(model: str) -> bool:
    return "qwen3" in (model or "").lower()


def _augment_system_for_qwen3(messages: list[dict]) -> list[dict]:
    """Append `/no_think` to qwen3 system prompts to disable thinking-mode.

    Qwen3 has a built-in switch: appending `/no_think` to a user or system
    message tells the model to skip its `<think>...</think>` reasoning block
    and answer directly. Without this on a 4B model + tool-calling, the model
    can spend its entire token budget thinking and produce empty answers.
    See: https://qwenlm.github.io/blog/qwen3/ ("Soft switch").
    """
    augmented: list[dict] = []
    injected = False
    for m in messages:
        if not injected and m.get("role") == "system":
            content = (m.get("content") or "")
            if "/no_think" not in content:
                content = f"{content}\n\n/no_think"
            augmented.append({**m, "content": content})
            injected = True
        else:
            augmented.append(m)
    if not injected:
        augmented.insert(0, {"role": "system", "content": "/no_think"})
    return augmented


async def _chat_openai_compatible(body: ChatStepIn, model: str, base_label: str) -> ChatStepOut:
    """Shared OpenAI / Ollama path — both expose the same chat-completions API."""
    client = _get_openai() if base_label == "openai" else _get_ollama()

    openai_messages = _msgs_to_openai(body.messages)
    if base_label == "ollama" and _is_qwen3(model):
        openai_messages = _augment_system_for_qwen3(openai_messages)

    resp = await client.chat.completions.create(
        model=model,
        max_tokens=CHAT_MAX_TOKENS,
        temperature=0.2,
        messages=openai_messages,
        tools=_tools_to_openai(body.available_tools),
        tool_choice="auto",
    )
    choice = resp.choices[0].message
    finish_reason = getattr(resp.choices[0], "finish_reason", None)

    tool_calls_raw = getattr(choice, "tool_calls", None) or []
    if tool_calls_raw:
        normalized = [
            ChatToolCall(
                id=tc.id,
                name=tc.function.name,
                arguments=_safe_json_loads(tc.function.arguments),
            )
            for tc in tool_calls_raw
        ]
        return ChatStepOut(type="tool_call", tool_calls=normalized, provider=base_label)

    raw = choice.content or ""
    after_think = _strip_thinking(raw)
    cleaned = _scrub_leaked_tool_calls(after_think)

    if not cleaned:
        # Diagnostics: log enough to debug small-model failures without leaking content.
        logger.info(
            "Empty chat reply (provider=%s, model=%s, finish=%s, raw_len=%d, after_think_len=%d)",
            base_label, model, finish_reason, len(raw), len(after_think),
        )
        if finish_reason == "length":
            cleaned = (
                "I needed more room to answer that. Try asking a more specific question — "
                "for example, name a single project or ask for one metric at a time."
            )
        elif raw and not after_think:
            cleaned = (
                "I was thinking that one through but didn't finish in time. "
                "Try rephrasing or breaking the question into smaller parts."
            )
        else:
            cleaned = "I'm here to help with your Simulyn projects. What would you like to know?"

    return ChatStepOut(
        type="answer",
        content=cleaned,
        provider=base_label,
    )


async def _chat_anthropic(body: ChatStepIn) -> ChatStepOut:
    client = _get_anthropic()
    system, msgs = _msgs_to_anthropic(body.messages)
    kwargs: dict = {
        "model": ANTHROPIC_MODEL,
        "max_tokens": CHAT_MAX_TOKENS,
        "temperature": 0.2,
        "messages": msgs,
        "tools": _tools_to_anthropic(body.available_tools),
    }
    if system:
        kwargs["system"] = system
    resp = await client.messages.create(**kwargs)

    text_parts: list[str] = []
    tool_uses: list[ChatToolCall] = []
    for block in resp.content:
        block_type = getattr(block, "type", None)
        if block_type == "text":
            text_parts.append(getattr(block, "text", ""))
        elif block_type == "tool_use":
            tool_uses.append(ChatToolCall(
                id=getattr(block, "id", ""),
                name=getattr(block, "name", ""),
                arguments=getattr(block, "input", {}) or {},
            ))
    if tool_uses:
        return ChatStepOut(type="tool_call", tool_calls=tool_uses, provider="anthropic")
    return ChatStepOut(
        type="answer",
        content="".join(text_parts).strip(),
        provider="anthropic",
    )


@app.post("/chat-step", response_model=ChatStepOut)
async def chat_step(body: ChatStepIn) -> ChatStepOut:
    if PROVIDER == "off":
        return ChatStepOut(
            type="answer",
            content=(
                "AI chat is not configured for this deployment. Ask your admin to set "
                "OPENAI_API_KEY, ANTHROPIC_API_KEY, or LLM_PROVIDER=ollama."
            ),
            provider="off",
        )

    try:
        if PROVIDER == "openai":
            return await _chat_openai_compatible(body, OPENAI_CHAT_MODEL, "openai")
        if PROVIDER == "ollama":
            return await _chat_openai_compatible(body, OLLAMA_CHAT_MODEL, "ollama")
        if PROVIDER == "anthropic":
            return await _chat_anthropic(body)
    except Exception as exc:
        chat_model = (
            OPENAI_CHAT_MODEL if PROVIDER == "openai"
            else OLLAMA_CHAT_MODEL if PROVIDER == "ollama"
            else ANTHROPIC_MODEL if PROVIDER == "anthropic"
            else "-"
        )
        logger.exception(
            "Chat step failed (provider=%s, chat_model=%s); error=%s",
            PROVIDER,
            chat_model,
            exc,
        )
        return ChatStepOut(
            type="answer",
            content=(
                "I hit an error talking to the language model. Try again in a moment, "
                "or ask your admin to check the AI service logs."
            ),
            provider=PROVIDER,
        )

    return ChatStepOut(
        type="answer",
        content="No LLM provider available.",
        provider=PROVIDER,
    )
