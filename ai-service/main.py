# ---------------------------------------------------------------------------
# LLM call (provider-agnostic, returns parsed JSON or None)
# ---------------------------------------------------------------------------

async def _llm_json(system_prompt: str, user_prompt: str) -> Optional[dict]:
    if PROVIDER == "off":
        return None

    try:
        if PROVIDER == "openai":
            client = _get_openai()
            resp = await client.chat.completions.create(
                model=OPENAI_MODEL,
                response_format={"type": "json_object"},
                max_tokens=LLM_MAX_TOKENS,
                temperature=0.2,
                messages=[
                    {"role": "system", "content": system_prompt},
                    {"role": "user", "content": user_prompt},
                ],
            )
            content = (resp.choices[0].message.content or "").strip()

        elif PROVIDER == "ollama":
            # Ollama exposes an OpenAI-compatible Chat Completions API.
            # response_format={"type":"json_object"} is honoured by Ollama
            # (translated to its native JSON mode) for models that support it.
            client = _get_ollama()
            resp = await client.chat.completions.create(
                model=OLLAMA_MODEL,
                response_format={"type": "json_object"},
                max_tokens=LLM_MAX_TOKENS,
                temperature=0.2,
                messages=[
                    {
                        "role": "system",
                        "content": (
                            system_prompt
                            + " Reply with raw JSON only. Do not wrap in markdown."
                        ),
                    },
                    {"role": "user", "content": user_prompt},
                ],
            )
            content = (resp.choices[0].message.content or "").strip()

        elif PROVIDER == "anthropic":
            client = _get_anthropic()
            resp = await client.messages.create(
                model=ANTHROPIC_MODEL,
                max_tokens=LLM_MAX_TOKENS,
                temperature=0.2,
                system=(
                    system_prompt
                    + " Respond with raw JSON only — no prose, no code fences."
                ),
                messages=[{"role": "user", "content": user_prompt}],
            )
            parts = []
            for block in resp.content:
                text = getattr(block, "text", None)
                if text:
                    parts.append(text)
            content = "".join(parts).strip()

        else:
            return None

        if content.startswith("```"):
            stripped = content.strip("`")
            if "\n" in stripped:
                stripped = stripped.split("\n", 1)[1]
            content = stripped.strip()

        return json.loads(content)

    except Exception as exc:
        logger.warning(
            "LLM call failed (provider=%s); falling back to deterministic text. error=%s",
            PROVIDER,
            exc,
        )
        return None


