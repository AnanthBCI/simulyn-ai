class AutoSuggestIn(BaseModel):
    project_id: str
    project_name: str
    project_start: str
    project_end: str
    task_count: int = Field(ge=0)
    high_risk_count: int = Field(ge=0)
    medium_risk_count: int = Field(ge=0)
    low_risk_count: int = Field(ge=0)
    avg_progress: int = Field(ge=0, le=100)
    expected_progress: int = Field(ge=0, le=100)
    days_to_finish: int
    tasks: list[dict] = Field(default_factory=list)  # [{id, name, progress}]


class SuggestedScenario(BaseModel):
    scenario_type: str
    label: str
    rationale: str
    config: dict


class AutoSuggestOut(BaseModel):
    suggestions: list[SuggestedScenario]


def _fallback_suggestions(body: AutoSuggestIn) -> list[SuggestedScenario]:
    out: list[SuggestedScenario] = []
    # Always suggest a small uniform slip — the "what if it just slips a week" baseline.
    out.append(SuggestedScenario(
        scenario_type="UniformSlip",
        label="What if every task slips 5 days?",
        rationale="A conservative baseline — small, uniform slippage across trades.",
        config={"InputDelayDays": 5},
    ))
    # Weather pause — common real-world hazard.
    out.append(SuggestedScenario(
        scenario_type="WeatherPause",
        label="What if a 7-day weather pause hits?",
        rationale="Sites with outdoor exposure should size the hit of a bad-weather week.",
        config={"PauseDays": 7},
    ))
    # If we have a laggy project, suggest adding resource.
    if body.expected_progress - body.avg_progress >= 10:
        out.append(SuggestedScenario(
            scenario_type="AddResource",
            label="What if we add 25% crew capacity?",
            rationale=(
                f"Progress is {body.expected_progress - body.avg_progress} pts behind "
                "plan; adding crew tests whether that recovers the slip."
            ),
            config={"CapacityMultiplier": 0.25},
        ))
    # If we have high-risk tasks, suggest slipping the first one as SingleTaskSlip.
    slip_task = next(iter(body.tasks), None)
    if slip_task:
        out.append(SuggestedScenario(
            scenario_type="SingleTaskSlip",
            label=f"What if \"{slip_task.get('name', 'the top task')}\" slips 5 days?",
            rationale="Isolating one task surfaces how critical-path sensitive the plan is.",
            config={"TaskId": slip_task.get("id"), "DelayDays": 5},
        ))
    # If scope is large, suggest scope reduction.
    if body.task_count >= 10:
        out.append(SuggestedScenario(
            scenario_type="ScopeReduction",
            label="What if we drop 2 tasks from scope?",
            rationale="Pressure test the schedule win from light de-scoping.",
            config={"TasksRemoved": 2},
        ))
    return out[:4]


AUTO_SUGGEST_SYSTEM_PROMPT = (
    "You are Simulyn AI, helping a construction project manager pick which "
    "what-if scenarios are worth simulating on their project right now. "
    "Choose scenarios that match the current state of the project (progress gap, "
    "risk levels, days to finish). Never invent project facts."
)


@app.post("/auto-suggest", response_model=AutoSuggestOut)
async def auto_suggest(body: AutoSuggestIn) -> AutoSuggestOut:
    suggestions = _fallback_suggestions(body)

    tasks_brief = ""
    if body.tasks:
        tasks_brief = "\nTop tasks (id, name, progress%):\n" + "\n".join(
            f"  - {t.get('id')}: {t.get('name')} ({t.get('progress', 0)}%)"
            for t in body.tasks[:6]
        )

    user_prompt = (
        f"Project: {body.project_name}\n"
        f"Window: {body.project_start} -> {body.project_end}\n"
        f"Days to finish: {body.days_to_finish}\n"
        f"Tasks: {body.task_count} total "
        f"(High={body.high_risk_count}, Medium={body.medium_risk_count}, Low={body.low_risk_count})\n"
        f"Progress: {body.avg_progress}% vs expected {body.expected_progress}%"
        f"{tasks_brief}\n\n"
        "Suggest 3-4 scenarios the PM should run this week. Respond with JSON:\n"
        '  {"suggestions": [{"scenario_type":..., "label":..., "rationale":..., "config": {...}}]}\n'
        "Allowed scenario_type values: UniformSlip, SingleTaskSlip, AddResource, WeatherPause, ScopeReduction.\n"
        "Config keys per type:\n"
        '  UniformSlip: {"InputDelayDays": int}\n'
        '  SingleTaskSlip: {"TaskId": <task_id from input>, "DelayDays": int}\n'
        '  AddResource: {"CapacityMultiplier": decimal 0.1-1.0}\n'
        '  WeatherPause: {"PauseDays": int}\n'
        '  ScopeReduction: {"TasksRemoved": int}\n'
        "label: short PM-facing question. rationale: one sentence why this matters now."
    )

    llm_data = await _llm_json(AUTO_SUGGEST_SYSTEM_PROMPT, user_prompt)
    if llm_data and isinstance(llm_data.get("suggestions"), list):
        parsed: list[SuggestedScenario] = []
        allowed = {"UniformSlip", "SingleTaskSlip", "AddResource", "WeatherPause", "ScopeReduction"}
        for item in llm_data["suggestions"]:
            if not isinstance(item, dict):
                continue
            st = str(item.get("scenario_type", "")).strip()
            label = str(item.get("label", "")).strip()
            rationale = str(item.get("rationale", "")).strip()
            config = item.get("config")
            if st not in allowed or not label or not isinstance(config, dict):
                continue
            parsed.append(SuggestedScenario(
                scenario_type=st,
                label=label[:140],
                rationale=rationale[:280],
                config=config,
            ))
        if parsed:
            suggestions = parsed[:5]

    return AutoSuggestOut(suggestions=suggestions)


