def _resolve_provider() -> ProviderName:
    if LLM_PROVIDER_ENV == "off":
        return "off"
    if LLM_PROVIDER_ENV == "ollama":
        return "ollama"  # always try; we'll fall back gracefully if unreachable
    if LLM_PROVIDER_ENV == "openai":
        return "openai" if OPENAI_API_KEY else "off"
    if LLM_PROVIDER_ENV == "anthropic":
        return "anthropic" if ANTHROPIC_API_KEY else "off"
    # auto: prefer hosted providers when keys are present, otherwise off.
    # Ollama is opt-in only because we don't probe at startup.
    if OPENAI_API_KEY:
        return "openai"
    if ANTHROPIC_API_KEY:
        return "anthropic"
    return "off"


