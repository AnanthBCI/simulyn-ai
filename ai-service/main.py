# /project-brief — one-glance project health narrative
#
# The health score is computed deterministically from the risk mix and the
# progress-vs-window gap so the same project renders the same score every
# time. The LLM only writes the headline, body, and tone tags.
# ---------------------------------------------------------------------------


class ProjectBriefIn(BaseModel):
    project_id: str
    project_name: str
    project_start: str
    project_end: str
    task_count: int = Field(ge=0)
    high_risk_count: int = Field(ge=0)
    medium_risk_count: int = Field(ge=0)
    low_risk_count: int = Field(ge=0)
    unpredicted_count: int = Field(ge=0)
    avg_progress: int = Field(ge=0, le=100)
    expected_progress: int = Field(ge=0, le=100)
    days_to_finish: int  # negative when past the planned end date


class ProjectBriefOut(BaseModel):
    headline: str
    body: str
    health_score: int
    tone_tags: list[str]


def _compute_health_score(body: ProjectBriefIn) -> int:
    """Return a 0-100 health score.

    Three roughly equal signals:
      - Risk mix  (High tasks hurt a lot, Medium a bit, Low not at all)
      - Progress gap (actual vs expected for the time elapsed)
      - Schedule cushion (how close we are to the planned end date)

    Unpredicted tasks get a mild penalty so "no data" doesn't read as "all good".
    """
    # Empty project — no tasks means no signal. Return a neutral, "you haven't
    # started yet" score instead of letting the math drift to a misleading 100
    # (every input ratio is 0/1 below). Keeps the dashboard from saying
    # "all healthy!" before the user has imported a single row.
    if body.task_count <= 0:
        return 50

    total = body.task_count

    high_ratio = body.high_risk_count / total
    med_ratio = body.medium_risk_count / total
    unpred_ratio = body.unpredicted_count / total
    risk_penalty = 100.0 * high_ratio + 40.0 * med_ratio + 15.0 * unpred_ratio
    risk_score = max(0.0, 100.0 - risk_penalty)

    gap = max(0, body.expected_progress - body.avg_progress)
    progress_score = max(0.0, 100.0 - gap * 2.0)

    if body.days_to_finish >= 14:
        cushion_score = 100.0
    elif body.days_to_finish >= 0:
        cushion_score = 60.0 + (body.days_to_finish / 14.0) * 40.0
    else:
        cushion_score = max(0.0, 60.0 + body.days_to_finish * 3.0)

    score = 0.4 * risk_score + 0.4 * progress_score + 0.2 * cushion_score
    return int(round(max(0.0, min(100.0, score))))


def _tone_tags_for(body: ProjectBriefIn, score: int) -> list[str]:
    tags: list[str] = []
    if score >= 80:
        tags.append("On track")
    elif score >= 55:
        tags.append("Watch")
    else:
        tags.append("At risk")

    if body.high_risk_count > 0:
        tags.append(f"{body.high_risk_count} high-risk task" + ("s" if body.high_risk_count != 1 else ""))
    if body.avg_progress + 10 < body.expected_progress:
        tags.append("Behind schedule")
    if body.days_to_finish < 0:
        tags.append("Past planned end")
    elif 0 <= body.days_to_finish <= 7:
        tags.append("Finish week")
    if body.unpredicted_count > 0 and body.unpredicted_count == body.task_count:
        tags.append("No predictions yet")
    return tags[:4]


def _fallback_brief_text(body: ProjectBriefIn, score: int) -> tuple[str, str]:
    if body.task_count == 0:
        return (
            f"{body.project_name}: no tasks yet.",
            "Add tasks or import a schedule to start tracking risk and progress.",
        )
    status = "on track" if score >= 80 else ("needs attention" if score >= 55 else "at risk")
    headline = f"{body.project_name}: {status}"
    parts = [
        f"Average progress {body.avg_progress}% vs expected {body.expected_progress}%.",
    ]
    if body.high_risk_count > 0:
        parts.append(
            f"{body.high_risk_count} task(s) at High risk, {body.medium_risk_count} at Medium."
        )
    if body.days_to_finish >= 0:
        parts.append(f"{body.days_to_finish} day(s) to planned finish.")
    else:
        parts.append(f"{-body.days_to_finish} day(s) past the planned finish.")
    return headline, " ".join(parts)


def _build_project_brief_user_prompt(body: ProjectBriefIn, score: int) -> str:
    return (
        f"Project: {body.project_name}\n"
        f"Window: {body.project_start} -> {body.project_end}\n"
        f"Days until planned finish: {body.days_to_finish}\n"
        f"Tasks: {body.task_count} total "
        f"(High={body.high_risk_count}, Medium={body.medium_risk_count}, "
        f"Low={body.low_risk_count}, Unpredicted={body.unpredicted_count})\n"
        f"Average progress: {body.avg_progress}% (expected {body.expected_progress}% for time elapsed)\n"
        f"Computed health score: {score}/100\n\n"
        "Respond with JSON containing exactly three fields:\n"
        '  "headline":  one short sentence (<= 90 chars), punchy, concrete.\n'
        '               Example: "Interior fitout slipping ~10 days, foundation clean."\n'
        '  "body":      2-3 sentences explaining the current state and the one thing a\n'
        "               busy PM should pay attention to. Use trade language where you can.\n"
        '  "tone_tags": JSON array of 1-3 short labels (<= 20 chars each), e.g.\n'
        '               ["Watch","Behind schedule"].'
    )


@app.post("/project-brief", response_model=ProjectBriefOut)
async def project_brief(body: ProjectBriefIn) -> ProjectBriefOut:
    score = _compute_health_score(body)
    headline, brief_body = _fallback_brief_text(body, score)
    tone_tags = _tone_tags_for(body, score)

    llm_data = await _llm_json(
        PROJECT_BRIEF_SYSTEM_PROMPT,
        _build_project_brief_user_prompt(body, score),
    )
    if llm_data:
        h = llm_data.get("headline")
        b = llm_data.get("body")
        t = llm_data.get("tone_tags")
        if isinstance(h, str) and h.strip():
            headline = h.strip()[:200]
        if isinstance(b, str) and b.strip():
            brief_body = b.strip()[:1800]
        if isinstance(t, list):
            cleaned = [str(x).strip()[:30] for x in t if isinstance(x, (str, int, float)) and str(x).strip()]
            if cleaned:
                tone_tags = cleaned[:4]

    return ProjectBriefOut(
        headline=headline,
        body=brief_body,
        health_score=score,
        tone_tags=tone_tags,
    )


