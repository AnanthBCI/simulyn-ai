def predict_delay(
    task_start: date,
    task_end: date,
    progress: int,
    today: Optional[date] = None,
) -> tuple[str, int, int, int]:
    """Return (risk_level, delay_days, expected_progress, gap_pct)."""
    t = today or date.today()
    total_days = max(1, (task_end - task_start).days)
    elapsed = max(0, min((t - task_start).days, total_days))
    expected = round(100.0 * elapsed / total_days) if total_days else 100
    gap = expected - progress

    if gap <= 5:
        risk = "Low"
        delay_days = max(0, gap // 5)
    elif gap <= 15:
        risk = "Medium"
        delay_days = max(1, gap // 3)
    else:
        risk = "High"
        delay_days = max(2, gap // 2)

    return risk, delay_days, expected, gap


def _fallback_predict_text(risk: str, expected: int, progress: int) -> tuple[str, str]:
    summary = f"Expected progress ~{expected}% vs actual {progress}%."
    if risk == "High":
        rec = (
            "• Add 2 workers\n"
            "• Increase working hours\n"
            "• Re-sequence dependent trades"
        )
    elif risk == "Medium":
        rec = "• Add 1 worker or extend shift\n• Review blockers daily"
    else:
        rec = "• Monitor weekly; no immediate action"
    return summary, rec


def _fallback_simulate_text(
    input_delay: int, predicted: int, new_end: date, task_count: int
) -> str:
    return (
        f"With an input slip of {input_delay} day(s), the model estimates "
        f"~{predicted} day(s) impact on the project finish "
        f"(new end ≈ {new_end.isoformat()}, {task_count} tasks in scope)."
    )


