def _build_provider_chain() -> list[ProviderConfig]:
    """Resolve the active fallback chain from env vars.

    Priority of inputs:
      1. `LLM_PROVIDERS` (CSV of provider names) — explicit order, wins.
      2. Legacy `LLM_PROVIDER` — single provider, or "off" / "auto".
      3. Default auto chain (each step is tried only if the previous failed):
           gemini → groq → openrouter → huggingface → openai → anthropic → ollama

    Rationale for the auto-chain order:
      * gemini, groq — free tiers, fast, used first to keep cost at $0
      * openrouter, huggingface — broader aggregators, multi-backend fallback
      * openai, anthropic — paid but reliable; jump in if everything else 429s
      * ollama — local model, last resort (always works when host is reachable)

    Each provider is silently skipped at startup if its API key isn't set, so
    the chain auto-shrinks to whatever you've actually configured.
    """
    if LLM_PROVIDERS_ENV:
        names = [n.strip() for n in LLM_PROVIDERS_ENV.split(",") if n.strip()]
    elif LLM_PROVIDER_ENV == "off":
        names = []
    elif LLM_PROVIDER_ENV in {"", "auto"}:
        names = [
            "gemini",
            "groq",
            "openrouter",
            "huggingface",
            "openai",
            "anthropic",
        ]
        if LLM_ENABLE_OLLAMA:
            names.append("ollama")
    else:
        names = [LLM_PROVIDER_ENV]

    chain: list[ProviderConfig] = []
    seen: set[str] = set()
    for n in names:
        if n in seen:
            continue
        seen.add(n)
        cfg = _config_for(n)
        if cfg:
            chain.append(cfg)
    return chain


