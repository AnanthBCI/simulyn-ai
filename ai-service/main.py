def _get_openai():
    """Returns an AsyncOpenAI client pointed at OPENAI_BASE_URL when set.

    OPENAI_BASE_URL unlocks any OpenAI-compatible provider without code changes:
    - Groq:      https://api.groq.com/openai/v1
    - DeepSeek:  https://api.deepseek.com/v1
    - Together:  https://api.together.xyz/v1
    - OpenRouter:https://openrouter.ai/api/v1
    - Azure:     https://<resource>.openai.azure.com/openai/deployments/<dep>
    """
    global _openai_client
    if _openai_client is None:
        from openai import AsyncOpenAI

        kwargs = {"api_key": OPENAI_API_KEY, "timeout": LLM_TIMEOUT_SECS}
        if OPENAI_BASE_URL:
            kwargs["base_url"] = OPENAI_BASE_URL
        _openai_client = AsyncOpenAI(**kwargs)
    return _openai_client


def _get_anthropic():
    global _anthropic_client
    if _anthropic_client is None:
        from anthropic import AsyncAnthropic

        _anthropic_client = AsyncAnthropic(api_key=ANTHROPIC_API_KEY, timeout=LLM_TIMEOUT_SECS)
    return _anthropic_client


def _get_ollama():
    """Reuse the OpenAI SDK against Ollama's OpenAI-compatible endpoint.
    Ollama accepts any non-empty api_key string."""
    global _ollama_client
    if _ollama_client is None:
        from openai import AsyncOpenAI

        _ollama_client = AsyncOpenAI(
            api_key="ollama",
            base_url=OLLAMA_BASE_URL,
            timeout=OLLAMA_TIMEOUT_SECS,
        )
    return _ollama_client


