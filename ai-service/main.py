class PredictIn(BaseModel):
    task_id: str
    task_name: str
    start_date: str
    end_date: str
    progress: int = Field(ge=0, le=100)
    project_start: str
    project_end: str


class PredictOut(BaseModel):
    risk_level: str
    delay_days: int
    summary: str
    recommendation: str


@app.post("/predict", response_model=PredictOut)
async def predict(body: PredictIn) -> PredictOut:
    ts, te = _parse(body.start_date), _parse(body.end_date)
    ps, pe = _parse(body.project_start), _parse(body.project_end)
    risk, delay_days, expected, gap = predict_delay(ts, te, body.progress)

    summary, recommendation = _fallback_predict_text(risk, expected, body.progress)

    llm_data = await _llm_json(
        PREDICT_SYSTEM_PROMPT,
        _build_predict_user_prompt(
            task_name=body.task_name,
            task_start=ts,
            task_end=te,
            progress=body.progress,
            expected=expected,
            gap=gap,
            risk=risk,
            delay_days=delay_days,
            project_start=ps,
            project_end=pe,
        ),
    )
    if llm_data:
        s = llm_data.get("summary")
        r = llm_data.get("recommendation")
        if isinstance(s, str) and s.strip():
            summary = s.strip()
        if isinstance(r, str) and r.strip():
            recommendation = r.strip()

    return PredictOut(
        risk_level=risk,
        delay_days=delay_days,
        summary=summary,
        recommendation=recommendation,
    )


class SimulateIn(BaseModel):
    """Scenario-aware what-if input.

    The deterministic math is done by the .NET side so every scenario has a
    predictable, auditable number. This service's only job is to dress the
    result in plain-English, trade-aware narrative.

    Legacy callers (pre-3A) can still pass `input_delay_days`/`task_count`
    without `scenario_type`; we treat that as UniformSlip.
    """

    project_id: str
    project_name: Optional[str] = None
    project_start: str
    project_end: str
    task_count: int = Field(ge=0)

    # New scenario-aware fields.
    scenario_type: Optional[str] = None
    config: Optional[dict[str, Any]] = None
    predicted_delay_days: Optional[int] = None
    deterministic_summary: Optional[str] = None
    signals: Optional[dict[str, Any]] = None

    # Legacy (kept for back-compat).
    input_delay_days: Optional[int] = Field(default=None, ge=0)


class SimulateOut(BaseModel):
    predicted_delay: int
    impact_summary: str
    headline: str
    scenario_type: str


def _scenario_hints(scenario_type: str) -> str:
    """Per-type guidance so the narrative reads differently for each scenario."""
    if scenario_type == "UniformSlip":
        return (
            "Scenario: every task slips by the same number of days. "
            "Focus on cumulative effect, trades most exposed to compression, "
            "and where resequencing could claw time back."
        )
    if scenario_type == "SingleTaskSlip":
        return (
            "Scenario: one specific task slips. Call the task out by name. "
            "Mention whether it sits on the critical path (late in the project) "
            "or has room to absorb through re-sequencing."
        )
    if scenario_type == "AddResource":
        return (
            "Scenario: extra crew / capacity has been added. Talk about which "
            "trades realistically scale (e.g. more formwork crews, more electricians) "
            "and the coordination cost that eats into nominal gains."
        )
    if scenario_type == "WeatherPause":
        return (
            "Scenario: weather window forces a pause. Focus on outdoor-exposed "
            "trades (earthworks, roofing, facade) and whether indoor scope can "
            "continue during the pause to soften the impact."
        )
    if scenario_type == "ScopeReduction":
        return (
            "Scenario: tasks have been dropped from scope. Acknowledge the "
            "schedule win but remind the PM of the quality / handover implications."
        )
    return ""


def _build_scenario_user_prompt(body: SimulateIn) -> str:
    scenario_type = body.scenario_type or "UniformSlip"
    predicted = body.predicted_delay_days if body.predicted_delay_days is not None else 0
    direction = (
        "later" if predicted > 0
        else "earlier" if predicted < 0
        else "unchanged"
    )
    signals_txt = ""
    if body.signals:
        parts = [f"  - {k}: {v}" for k, v in body.signals.items()]
        signals_txt = "\nDeterministic signals:\n" + "\n".join(parts)

    return (
        f"Project: {body.project_name or body.project_id}\n"
        f"Project window: {body.project_start} -> {body.project_end}\n"
        f"Tasks in scope: {body.task_count}\n"
        f"Scenario type: {scenario_type}\n"
        f"Deterministic result: {predicted:+d} day(s) ({direction})\n"
        f"{_scenario_hints(scenario_type)}"
        f"{signals_txt}\n"
        "\nDeterministic one-liner (rewrite in your own words, don't quote verbatim):\n"
        f"  {body.deterministic_summary or ''}\n\n"
        "Respond with JSON containing exactly two string fields:\n"
        '  "headline":       short card title, <= 90 chars. Name the trade or task\n'
        '                    where relevant. Example: "Wiring slip + 3 day finish".\n'
        '  "impact_summary": 2-4 sentences: what this scenario means, who is most\n'
        "                    affected, and one or two concrete mitigations.\n"
        "                    Plain text, no markdown, no restating the integer day count."
    )


@app.post("/simulate", response_model=SimulateOut)
async def simulate(body: SimulateIn) -> SimulateOut:
    scenario_type = body.scenario_type or "UniformSlip"
    ps, pe = _parse(body.project_start), _parse(body.project_end)

    # Legacy back-compat: if the caller didn't pre-compute a predicted delay,
    # fall back to the old UniformSlip math here so pre-3A demos still work.
    if body.predicted_delay_days is None:
        input_delay = body.input_delay_days or 0
        predicted = round(input_delay * (1 + body.task_count * 0.05))
        det_summary = (
            body.deterministic_summary
            or _fallback_simulate_text(
                input_delay, predicted, pe + timedelta(days=predicted), body.task_count
            )
        )
    else:
        predicted = body.predicted_delay_days
        det_summary = body.deterministic_summary or ""

    # Fallback narrative — used when the LLM is off or unreachable.
    headline = _fallback_headline(scenario_type, predicted)
    impact = det_summary or _fallback_simulate_text(
        body.input_delay_days or 0, predicted, pe + timedelta(days=max(0, predicted)), body.task_count
    )

    llm_data = await _llm_json(
        SIMULATE_SYSTEM_PROMPT,
        _build_scenario_user_prompt(body),
    )
    if llm_data:
        h = llm_data.get("headline")
        s = llm_data.get("impact_summary")
        if isinstance(h, str) and h.strip():
            headline = h.strip()[:200]
        if isinstance(s, str) and s.strip():
            impact = s.strip()

    return SimulateOut(
        predicted_delay=predicted,
        impact_summary=impact,
        headline=headline,
        scenario_type=scenario_type,
    )


def _fallback_headline(scenario_type: str, predicted: int) -> str:
    if scenario_type == "UniformSlip":
        return f"Uniform slip → {predicted:+d} day finish"
    if scenario_type == "SingleTaskSlip":
        return f"Single task slip → {predicted:+d} day finish"
    if scenario_type == "AddResource":
        return f"Add resource → {predicted:+d} day finish"
    if scenario_type == "WeatherPause":
        return f"Weather pause → {predicted:+d} day finish"
    if scenario_type == "ScopeReduction":
        return f"Scope reduction → {predicted:+d} day finish"
    return f"Scenario → {predicted:+d} day finish"


