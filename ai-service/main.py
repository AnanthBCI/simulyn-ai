"""
Simulyn AI — Risk engine + LLM-powered narrative + chat copilot.

Numeric outputs (risk_level, delay_days, predicted_delay) are computed by a
deterministic rule engine so they are predictable and auditable. The narrative
fields (summary, recommendation, impact_summary) are generated by an LLM
(OpenAI, Anthropic, or a local Ollama server) when configured, otherwise we
fall back to canned text so local dev still works.

The /chat-step endpoint powers the multilingual chat copilot. It is stateless:
it receives the full conversation + available tool schemas, asks the LLM for
one step (tool_call or final answer), and returns it. The .NET orchestrator
executes tools in-process and loops back. See backend ChatOrchestrator.cs.

Environment variables:
  LLM_PROVIDER         openai | anthropic | ollama | auto | off   (default: auto)
  OPENAI_API_KEY       enables OpenAI provider (also: Groq / DeepSeek / Together /
                       OpenRouter — any OpenAI-compatible API, via OPENAI_BASE_URL)
  ANTHROPIC_API_KEY    enables Anthropic provider
  OPENAI_BASE_URL      default: unset (→ https://api.openai.com/v1). Override to use
                       any OpenAI-compatible endpoint: Groq, DeepSeek, Together, etc.
  OPENAI_MODEL         default: gpt-4o-mini (narrative: /predict, /simulate,
                       /project-brief, /weekly-recap)
  OPENAI_CHAT_MODEL    default: inherits OPENAI_MODEL. Override to use a different
                       model for /chat-step (e.g. a larger tool-capable model).
  ANTHROPIC_MODEL      default: claude-3-5-haiku-latest
  OLLAMA_BASE_URL      default: http://localhost:11434/v1
  OLLAMA_MODEL         default: llama3.2 (for /predict, /simulate, and /chat-step
                       when OLLAMA_CHAT_MODEL is not set)
  OLLAMA_CHAT_MODEL    default: inherits OLLAMA_MODEL. Override with a tool-capable
                       model like llama3.1:8b or qwen2.5:7b for better chat quality.
  OLLAMA_TIMEOUT_SECS  default: 60   (local CPU inference can be slow)
  LLM_TIMEOUT_SECS     default: 8    (used for OpenAI / Anthropic)
  LLM_MAX_TOKENS       default: 350
  CHAT_MAX_TOKENS      default: 700  (chat replies can be longer than predict)
"""
