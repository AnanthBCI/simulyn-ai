# Cache OpenAI/Anthropic clients keyed by (kind, api_key, base_url). One Gemini
# call and one Groq call use distinct clients without paying SDK init twice.
_client_cache: dict[tuple[str, Optional[str], Optional[str]], Any] = {}


def _get_client(cfg: ProviderConfig):
    key = (cfg.kind, cfg.api_key, cfg.base_url)
    client = _client_cache.get(key)
    if client is not None:
        return client

    if cfg.kind == "openai_compat":
        from openai import AsyncOpenAI

        kwargs: dict[str, Any] = {"api_key": cfg.api_key, "timeout": cfg.timeout}
        if cfg.base_url:
            kwargs["base_url"] = cfg.base_url
        # OpenRouter recommends attribution headers so usage appears on your
        # dashboard and (optionally) in their public leaderboard. Both are
        # optional from a functionality standpoint — they don't gate access.
        if cfg.name == "openrouter":
            kwargs["default_headers"] = {
                "HTTP-Referer": OPENROUTER_HTTP_REFERER,
                "X-Title": OPENROUTER_X_TITLE,
            }
        client = AsyncOpenAI(**kwargs)
    elif cfg.kind == "anthropic":
        from anthropic import AsyncAnthropic

        client = AsyncAnthropic(api_key=cfg.api_key, timeout=cfg.timeout)
    else:
        raise ValueError(f"Unknown provider kind: {cfg.kind}")

    _client_cache[key] = client
    return client


# --- Generic chain runner -------------------------------------------------

_T = TypeVar("_T")


def _short_error(exc: BaseException, limit: int = 240) -> str:
    """Render an exception as a single short line for log readability.

    Provider SDKs raise exceptions whose ``str()`` includes huge JSON blobs
    (full request id, full upstream error envelope). For operator logs we
    only want the human-readable reason — first line, trimmed.
    """
    msg = str(exc).strip() or repr(exc)
    first_line = msg.splitlines()[0] if msg else "<no message>"
    return first_line if len(first_line) <= limit else first_line[:limit] + "…"


async def _run_chain(
    label: str,
    op: Callable[[ProviderConfig], Awaitable[Optional[_T]]],
) -> Optional[_T]:
    """Walk the provider chain, calling `op(cfg)` until one returns a non-None
    result. Logs per-attempt failures at info-level so operators can see which
    provider answered (or which failed) without flooding warn-level logs.
    """
    for cfg in PROVIDER_CHAIN:
        try:
            result = await op(cfg)
        except Exception as exc:  # noqa: BLE001 — provider SDKs raise many types
            # Logged at WARNING (not INFO) so it surfaces in default-level
            # production logs. Includes the exception message so operators
            # can distinguish 401/403 (bad key) from 429 (rate limit) from
            # timeouts without enabling debug logging.
            logger.warning(
                "%s: provider=%s model=%s failed (%s: %s) — trying next",
                label,
                cfg.name,
                cfg.model,
                type(exc).__name__,
                _short_error(exc),
            )
            continue
        if result is not None:
            logger.info("%s: provider=%s model=%s ok", label, cfg.name, cfg.model)
            return result
        logger.info(
            "%s: provider=%s model=%s returned no usable content — trying next",
            label,
            cfg.name,
            cfg.model,
        )
    return None


# --- Legacy single-provider accessors (kept for back-compat) ---------------
#
# A few code paths (e.g. tests, /health) still reference these. They now look
# up the head of the chain to keep behaviour consistent.

def _head_of(kind: ProviderKind) -> Optional[ProviderConfig]:
    for cfg in PROVIDER_CHAIN:
        if cfg.kind == kind:
            return cfg
    return None


def _get_openai():
    """Legacy accessor — first OpenAI-compatible provider in the chain."""
    cfg = _head_of("openai_compat")
    if cfg is None:
        raise RuntimeError("No OpenAI-compatible provider configured.")
    return _get_client(cfg)


def _get_anthropic():
    cfg = _head_of("anthropic")
    if cfg is None:
        raise RuntimeError("Anthropic is not configured.")
    return _get_client(cfg)


def _get_ollama():
    cfg = _config_for("ollama")
    if cfg is None:
        raise RuntimeError("Ollama provider unavailable.")
    return _get_client(cfg)


