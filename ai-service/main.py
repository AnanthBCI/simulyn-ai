# ---------------------------------------------------------------------------
# LLM call (provider-agnostic, returns parsed JSON or None)
# ---------------------------------------------------------------------------

def _clean_json_text(content: str) -> str:
    """Strip ```json ... ``` wrappers some models still emit despite asking for
    raw JSON. Idempotent for already-clean payloads."""
    content = content.strip()
    if content.startswith("```"):
        stripped = content.strip("`")
        if "\n" in stripped:
            stripped = stripped.split("\n", 1)[1]
        content = stripped.strip()
    return content


async def _llm_json_via(
    cfg: ProviderConfig, system_prompt: str, user_prompt: str
) -> Optional[dict]:
    """Single-provider JSON call. Returns parsed dict on success, None when the
    provider responded but the content wasn't usable; raises on transport errors
    so the chain runner moves on."""
    if cfg.kind == "openai_compat":
        client = _get_client(cfg)
        # Ollama / older models need an extra nudge to stick to raw JSON.
        nudge = (
            ""
            if cfg.name in {"gemini", "groq", "openai"}
            else " Reply with raw JSON only. Do not wrap in markdown."
        )
        resp = await client.chat.completions.create(
            model=cfg.model,
            response_format={"type": "json_object"},
            max_tokens=LLM_MAX_TOKENS,
            temperature=0.2,
            messages=[
                {"role": "system", "content": system_prompt + nudge},
                {"role": "user", "content": user_prompt},
            ],
        )
        content = (resp.choices[0].message.content or "").strip()

    elif cfg.kind == "anthropic":
        client = _get_client(cfg)
        resp = await client.messages.create(
            model=cfg.model,
            max_tokens=LLM_MAX_TOKENS,
            temperature=0.2,
            system=(
                system_prompt
                + " Respond with raw JSON only — no prose, no code fences."
            ),
            messages=[{"role": "user", "content": user_prompt}],
        )
        parts: list[str] = []
        for block in resp.content:
            text = getattr(block, "text", None)
            if text:
                parts.append(text)
        content = "".join(parts).strip()
    else:
        return None

    content = _clean_json_text(content)
    if not content:
        return None

    try:
        return json.loads(content)
    except json.JSONDecodeError as exc:
        logger.info(
            "LLM JSON parse failed (provider=%s, model=%s): %s",
            cfg.name,
            cfg.model,
            exc,
        )
        return None


async def _llm_json(system_prompt: str, user_prompt: str) -> Optional[dict]:
    """Try each provider in the configured chain until one returns parseable
    JSON. Returns None when all providers fail (caller falls back to its
    deterministic text)."""
    if not PROVIDER_CHAIN:
        return None
    return await _run_chain(
        "llm_json", lambda cfg: _llm_json_via(cfg, system_prompt, user_prompt)
    )


